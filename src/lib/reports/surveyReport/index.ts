function photosSection(doc: jsPDF, photos: Photo[]): void {
  sectionTitle(doc, 'Field Photographs', 'P')
  bodyText(doc, `The following ${photos.length} field photograph(s) were taken during the survey.`)
  _yPos += 4

  const cols = photos.length >= 4 ? 2 : 1
  const imgW = cols === 2 ? 85 : 175
  const imgH = imgW * 0.75

  for (let i = 0; i < photos.length; i += cols) {
    newPage(doc)
    const row = photos.slice(i, i + cols)
    for (let j = 0; j < row.length; j++) {
      const photo = row[j]
      try {
        doc.addImage(photo.url, 'JPEG', 15 + j * (imgW + 5), _yPos, imgW, imgH)
        _yPos += imgH + 4
        doc.setFontSize(8)
        doc.setFont('helvetica', 'bold')
        doc.setTextColor(...AMBER)
        doc.text(`Photo ${i + j + 1}`, 15 + j * (imgW + 5), _yPos)
        _yPos += 4
        doc.setFont('helvetica', 'normal')
        doc.setTextColor(30, 30, 30)
        const caption = photo.caption || 'Field photograph'
        const captionLines = doc.splitTextToSize(caption, imgW)
        doc.text(captionLines, 15 + j * (imgW + 5), _yPos)
        _yPos += captionLines.length * 4
        if (photo.orientation) {
          doc.setFontSize(7)
          doc.setTextColor(120, 120, 120)
          doc.text(`View: ${photo.orientation}`, 15 + j * (imgW + 5), _yPos)
          _yPos += 4
        }
      } catch {
        doc.setFontSize(9)
        doc.setTextColor(120, 120, 120)
        doc.text(`[Photo ${i + j + 1}: ${photo.caption || 'image unavailable'}]`, 15, _yPos)
        _yPos += 8
      }
    }
    if (cols === 2) _yPos += imgH
  }
}

export function generateSurveyReport(
  data: SurveyReportData,
  opts: SurveyReportOptions = {},
  onBlob?: (blob: Blob, filename: string) => void
): void {
  // Pass submission_number to footers
  const pageFooterOpts = { submission_number: opts.submission_number }
  reset()
  const doc = new jsPDF({ orientation: 'portrait', unit: 'mm', format: 'a4' })

  coverPage(doc, data, opts)

  addPageFooter(doc)
  doc.addPage()
  _currentPage++

  tableOfContents(doc, data)

  addPageFooter(doc)
  doc.addPage()
  _currentPage++
  _yPos = 20

  introductionSection(doc, data, opts)
  methodologySection(doc, data)
  controlNetworkSection(doc, data)

  if (data.traverse) {
    traverseComputationSection(doc, data)
  }

  boundaryDescriptionSection(doc, data)
  beaconDescriptionSection(doc, data)
  topographicFeaturesSection(doc, data)
  calculationMethodologySection(doc, data)
  authenticationSection(doc, data)
  appendicesSection(doc)

  if (opts.includeMobilisation && data.mobilisation) {
    addPageFooter(doc)
    doc.addPage()
    _currentPage++
    _yPos = 20
    mobilisationSection(doc, data.mobilisation)
  }

  if (opts.includePhotos && data.photos?.length) {
    addPageFooter(doc)
    doc.addPage()
    _currentPage++
    _yPos = 20
    photosSection(doc, data.photos)
  }

  const totalPages = doc.getNumberOfPages()
  for (let i = 1; i <= totalPages; i++) {
    doc.setPage(i)
    addPageFooter(doc)
  }

  const date = new Date().toISOString().slice(0, 10)
  const filename = `${data.project.name.replace(/\s+/g, '_')}_Survey_Report_${date}.pdf`

  if (onBlob) {
    const blob = doc.output('blob')
    onBlob(blob, filename)
  } else {
    doc.save(filename)
  }
}
