const AMBER: [number, number, number] = [232, 132, 26]
const DARK: [number, number, number] = [15, 15, 20]
const WHITE: [number, number, number] = [255, 255, 255]
const GRAY: [number, number, number] = [120, 120, 120]
const LIGHT_GRAY: [number, number, number] = [240, 240, 240]

let _yPos = 20
let _currentPage = 1

function reset() {
  _yPos = 20
  _currentPage = 1
}

function newPage(doc: jsPDF, minY = 260): void {
  if (_yPos > minY) {
    addPageFooter(doc)
    doc.addPage()
    _currentPage++
    _yPos = 20
  }
}

function checkPage(doc: jsPDF): void {
  if (_yPos > 260) {
    addPageFooter(doc)
    doc.addPage()
    _currentPage++
    _yPos = 20
  }
}

function addPageFooter(doc: jsPDF, opts?: { submission_number?: string }): void {
  doc.setFillColor(...DARK)
  doc.rect(0, 283, 210, 14, 'F')
  doc.setTextColor(...GRAY)
  doc.setFontSize(7)
  doc.setFont('helvetica', 'normal')
  doc.text('Generated by METARDU — Professional Surveying Platform', 15, 290)
  if (opts?.submission_number) {
    doc.text(opts.submission_number, 105, 290, { align: 'center' })
  }
  doc.text(`Page ${_currentPage}`, 195, 290, { align: 'right' })
}

function sectionTitle(doc: jsPDF, text: string, number?: string): void {
  checkPage(doc)
  doc.setTextColor(...AMBER)
  doc.setFontSize(12)
  doc.setFont('helvetica', 'bold')
  if (number) {
    doc.text(`${number}. ${text}`, 15, _yPos)
  } else {
    doc.text(text, 15, _yPos)
  }
  _yPos += 4
  doc.setDrawColor(...AMBER)
  doc.setLineWidth(0.5)
  doc.line(15, _yPos, 195, _yPos)
  _yPos += 5
}

function bodyText(doc: jsPDF, text: string, maxWidth = 180): void {
  checkPage(doc)
  doc.setTextColor(30, 30, 30)
  doc.setFontSize(9)
  doc.setFont('helvetica', 'normal')
  const lines = doc.splitTextToSize(text, maxWidth)
  doc.text(lines, 15, _yPos)
  _yPos += lines.length * 4.5 + 3
}

function labelValue(doc: jsPDF, label: string, value: string, indent = 15): void {
  checkPage(doc)
  doc.setFontSize(9)
  doc.setFont('helvetica', 'bold')
  doc.setTextColor(...AMBER)
  doc.text(label, indent, _yPos)
  doc.setFont('helvetica', 'normal')
  doc.setTextColor(30, 30, 30)
  doc.text(value || '—', indent + 38, _yPos)
  _yPos += 5
}

